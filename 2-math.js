let number1 = 13
let number2 = 56

// Toplama
console.log("Toplama : " + (number1 + number2))
// Çıkarma
console.log("Çıkarma : " + (number1 - number2))
// Çarpa
console.log("Çaprma : " + (number1 * number2))
// Bölme
console.log("Bölme : " + (number1 / number2))

// Modunu alma (Bölümünden kalanını görmek)
console.log("Kalan : " + (number2 % number1))

// Sayının kuvvetini hesaplama
console.log("Sayının ilgili kuvveti : " + 4 ** 2)

// Tek tek arttırma
console.log(++number2) // number++
// Tek tek azaltma
console.log(--number1) // number--

